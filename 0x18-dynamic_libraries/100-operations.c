/**
 * add - a + b
 * @a: first number
 * @b: second number
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - a - b
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int sub(int a, int b)
{
  return (a - b);
}

/**
 * mul - a * b
 * @a: first number
 * @b: second number
 * Return: a * b
 */
int mul(int a, int b)
{
  return (a + b);
}

/**
 * div - a / b
 * @a: first number
 * @b: second number
 * Return: a / b
 */
int div(int a, int b)
{
  return (a / b);
}

/**
 * mod - a % b
 * @a: first number
 * @b: second number
 * Return: a % b
 */
int mod(int a, int b)
{
  return (a % b);
}
